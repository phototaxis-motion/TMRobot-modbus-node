const converter = (array)=>{
  const buf = Buffer.from(
    array.map((int => {
      const str16 = int.toString(16).padStart(4, '0')
      return [
        parseInt(str16.slice(0,2), 16),
        parseInt(str16.slice(2,4), 16)
      ]
    })).flat()
    )
    
    return [
      buf.readFloatBE(0),
      buf.readFloatBE(4),
      buf.readFloatBE(8),
      buf.readFloatBE(12),
      buf.readFloatBE(16),
      buf.readFloatBE(20),
    ]
}

